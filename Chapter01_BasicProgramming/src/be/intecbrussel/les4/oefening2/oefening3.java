package be.intecbrussel.les3.oefening2;

/*Gebruik de Relational operators op de variabelen hieronder.
Bekijk alle mogelijkheden om deze te gebruiken.

        byte aNum = 10;
        int bNum = 5;
        long cNum = 5;
  */
public class oefening3 {
    public static void main(String[] args) {

        byte aNum = 10;
        int bNum = 5;
        long cNum = 5;

        System.out.println("aNum < bNum: " + (aNum < bNum));
        System.out.println("aNum <= bNum: " + (aNum <= bNum));
        System.out.println("aNum > bNum: " + (aNum > bNum));
        System.out.println("aNum >= bNum: " + (aNum >= bNum));
        System.out.println("bNum == cNum: " + (bNum == cNum));
        System.out.println("bNum != cNum: " + (bNum != cNum));

    }
}
