package be.intecbrussel.les2;

public class AssegnementExemple {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        int c=10;

        c=(a+b);
        System.out.println(c);

        c+=a;
        System.out.println(c);

        c-=a;
        System.out.println(c);

        c*=a;
        System.out.println(c);

        c/=a;
        System.out.println(c);

        c%=a;
        System.out.println(c);
    }
}
